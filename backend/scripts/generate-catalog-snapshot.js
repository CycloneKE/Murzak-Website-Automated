/**
 * generate-catalog-snapshot.js
 *
 * The customer-facing service catalog lives in the frontend
 * (frontend/src/config/serviceCatalog.ts) and is the single source of truth.
 * The backend needs a *lean* view of it for provisioning and billing — id ->
 * name, category, capacityClass, resource footprint (ramMb/diskGb) and retail
 * price (monthlyKes/setupKes) — to route/size jobs and price invoices without
 * duplicating (and drifting from) the catalog.
 *
 * Rather than hand-duplicate (and drift), this script parses the TS file and
 * emits backend/data/serviceCatalogSnapshot.json. Re-run it whenever the catalog
 * changes:
 *
 *   node backend/scripts/generate-catalog-snapshot.js
 *
 * It is a deliberately simple regex extractor — it relies on the catalog's
 * one-field-per-line shape, not a full TS parser.
 */

const fs = require("fs");
const path = require("path");

const SRC = path.resolve(
  __dirname,
  "../../frontend/src/config/serviceCatalog.ts"
);
const OUT = path.resolve(__dirname, "../data/serviceCatalogSnapshot.json");

function pull(block, key) {
  // matches  key: "value"   or   key: value
  const re = new RegExp(`${key}\\s*:\\s*("([^"]*)"|([0-9]+))`);
  const m = block.match(re);
  if (!m) return undefined;
  return m[2] !== undefined ? m[2] : Number(m[3]);
}

// matches  export const NAME = 1234  (top-level numeric constant)
function pullConst(text, name) {
  const m = text.match(new RegExp(`${name}\\s*=\\s*([0-9]+)`));
  return m ? Number(m[1]) : undefined;
}

function main() {
  const text = fs.readFileSync(SRC, "utf8");

  // Find every `id: "..."` and treat the text up to the next `id:` (or EOF)
  // as that item's block. Field names (name/category/capacityClass/ramMb/diskGb)
  // appear once per item before the next id, so a first-match pull is safe.
  const idRe = /id\s*:\s*"([^"]+)"/g;
  const positions = [];
  let m;
  while ((m = idRe.exec(text)) !== null) {
    positions.push({ id: m[1], start: m.index });
  }

  const items = {};
  for (let i = 0; i < positions.length; i++) {
    const { id, start } = positions[i];
    const end = i + 1 < positions.length ? positions[i + 1].start : text.length;
    const block = text.slice(start, end);

    const name = pull(block, "name");
    const category = pull(block, "category");
    const tier = pull(block, "tier");
    const capacityClass = pull(block, "capacityClass");
    const ramMb = pull(block, "ramMb");
    const diskGb = pull(block, "diskGb");
    const pricingModel = pull(block, "model");
    const monthlyKes = pull(block, "monthlyKes");
    const setupKes = pull(block, "setupKes");
    // Boolean flag (pull() only handles strings/numbers): BYOA services that
    // deploy the customer's own repo — provisioning needs this to attach the
    // repo URL to the job.
    const requiresRepo = /requiresRepo\s*:\s*true/.test(block);

    // Skip anything that isn't a real catalog service (must at least name itself).
    if (!name || !category || !capacityClass) continue;

    items[id] = {
      id,
      name,
      category,
      tier: tier || null,
      capacityClass,
      ramMb: ramMb == null ? null : Number(ramMb),
      diskGb: diskGb == null ? null : Number(diskGb),
      pricingModel: pricingModel || null,
      monthlyKes: monthlyKes == null ? 0 : Number(monthlyKes),
      setupKes: setupKes == null ? 0 : Number(setupKes),
      requiresRepo,
    };
  }

  // Capacity budget — mirror SERVER_CAPACITY from the same source file.
  const capBlock = text.slice(text.indexOf("export const SERVER_CAPACITY"));
  const capacity = {
    plan: pull(capBlock, "plan"),
    totalRamMb: pull(capBlock, "totalRamMb"),
    totalDiskGb: pull(capBlock, "totalDiskGb"),
    vcpu: pull(capBlock, "vcpu"),
    bandwidthTb: pull(capBlock, "bandwidthTb"),
    sellableRamMb: pull(capBlock, "sellableRamMb"),
    sellableDiskGb: pull(capBlock, "sellableDiskGb"),
    // Per-self-serve-order caps (top-level consts in the same file) so the
    // backend order guard reads the SAME numbers the configurator enforces.
    selfServeOrderRamCapMb: pullConst(text, "SELF_SERVE_ORDER_RAM_CAP_MB"),
    selfServeOrderDiskCapGb: pullConst(text, "SELF_SERVE_ORDER_DISK_CAP_GB"),
  };

  const snapshot = {
    generatedFrom: "frontend/src/config/serviceCatalog.ts",
    note: "Generated by backend/scripts/generate-catalog-snapshot.js — do not edit by hand.",
    capacity,
    items,
  };

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, JSON.stringify(snapshot, null, 2) + "\n", "utf8");

  const count = Object.keys(items).length;
  console.log(`Wrote ${count} services to ${path.relative(process.cwd(), OUT)}`);
}

main();
